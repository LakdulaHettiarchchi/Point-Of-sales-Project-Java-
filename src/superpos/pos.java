  private void pointOfsales()
  {
  
    String name = txtprocode.getText();  /////bar code eka gannwa/////////
        try {
             Class.forName("com.mysql.jdbc.Driver");
             con1 = DriverManager.getConnection("jdbc:mysql://localhost/superpos","root","");
             pst =con1.prepareStatement("select * from product where barcode = ?" );
                
            pst.setString(1, name);
            rs = pst.executeQuery();
       
        
            while(rs.next())
            {
            
            int currentqty;
            
              currentqty = rs.getInt("qty");////denata tiyana qty eka(database eke tiyana gana)
              
              int price = Integer.parseInt(txtprice.getText());
              int qty_new = Integer.parseInt( txtqty.getText()); ////api enter karana qty eka//////
              
              int tot = price * qty_new;
              
              if(qty_new >= currentqty){
               
                 JOptionPane.showMessageDialog(this, "Avilable product" + "=" + currentqty);
                 JOptionPane.showMessageDialog(this, "Qyt is not Enough ");
                 
                 
               
               }
              
              else
              {
               model = (DefaultTableModel)jTable1.getModel();
               model.addRow(new Object[]
                       
               {
              
                txtprocode.getText(),
                txtproname.getText(),    
                txtprice.getText(),    
                txtqty.getText(),
                tot,
               
               });
               
           int sum=0;
             
           for(int i=1; i<jTable1.getRowCount(); i++)
            {
              
           sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
              
            }
   
            
              
              txtsub.setText(Integer.toString(sum));
               
               
               txtprocode.setText("");
               txtproname.setText(""); 
               txtprice.setText(""); 
               txtqty.setText("");
               txtprocode.setText("");
               
               txtprocode.requestFocus();
               
        
              
              }
              
            }
            
            
            
            
            
        } catch (ClassNotFoundException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            }
             
  
  
  
  
  
  }

