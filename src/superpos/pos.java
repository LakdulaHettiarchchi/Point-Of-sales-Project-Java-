    private void txtprocodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtprocodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){ /////if press the EnterKey
        
           String name = txtprocode.getText();  /////bar code eka gannwa/////////
           
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/superpos","root","");
                
                 
                pst =con1.prepareStatement("select * from product where barcode = ?" );
                
                pst.setString(1, name);
                rs = pst.executeQuery();
                
                if(rs.next()==false){  
                  
                   JOptionPane.showMessageDialog(this,"Barcode is not found");
                   txtprocode.setText("");
                   
                 }
                
                else{
                
                   String productname = rs.getString("product");
                   String price = rs.getString("retail_price");
                 
                   txtproname.setText(productname.trim());
                   txtprice.setText(price.trim());
                   
             }
                
                
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            }
           
             

           
        
        
        }
    }//GEN-LAST:event_txtprocodeKeyPressed

